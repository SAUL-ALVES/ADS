function calculadoraSimples(num1, num2, operacao){
    if (operacao == '+'){
        return num1 + num2
    }
    else if (operacao == '-'){
        return num1 - num2
    }
    else if (operacao == '/'){
        return num1 / num2
    }
    else if(operacao == '*'){
        return num1 * num2
    }
}

